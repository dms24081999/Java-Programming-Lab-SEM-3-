package Exp1;

public class PrimitiveDataTypes {

    static boolean b;
    static int i;
    static char c;
    static float f;
    static double d;
    static long l;
    static short s;
    static byte b1;

    public static void main(String[] args) {
        System.out.println("Default value of Boolean is: " + b);
        System.out.println("Default value of Integer is: " + i);
        System.out.println("Default value of Character is: " + c);
        System.out.println("Default value of Float is: " + f);
        System.out.println("Default value of Double is: " + d);
        System.out.println("Default value of Long is: " + l);
        System.out.println("Default value of Short is: " + s);
        System.out.println("Default value of Byte is: " + b1);
    }
}
